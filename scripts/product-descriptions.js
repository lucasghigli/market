export function getDescriptionForProduct(name, category) {
  const normalized = normalizeName(name);

  for (const [keyword, description] of keywordDescriptions) {
    if (normalized.includes(keyword)) {
      return description;
    }
  }

  const templates = categoryTemplates[category] || categoryTemplates.fruits;
  const template = templates[hashName(name) % templates.length];
  return template(name);
}
